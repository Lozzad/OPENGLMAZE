void View::update() {

	do {
		// Clear the screen. It's not mentioned before Tutorial 02, but it can cause flickering, so it's there nonetheless.
		glClear(GL_COLOR_BUFFER_BIT | GL_DEPTH_BUFFER_BIT);

		MatrixID1 = glGetUniformLocation(programID, "MVP");
		computeMatricesFromInputs(window, height, width);

		glm::mat4 mvp1 = getMVPMatrix(
			getProjectionMatrix(),
			getViewMatrix(),
			glm::mat4(1.0f)
		);
		glUniformMatrix4fv(MatrixID1, 1, GL_FALSE, &mvp1[0][0]);

		//use our shaders
		glUseProgram(programID);

		//1st attribute buffer is verts
		glEnableVertexAttribArray(0);
		glBindBuffer(GL_ARRAY_BUFFER, vertexbuffer);
		glVertexAttribPointer(
			0,			//key, must be same as vertex shader
			3,			//size
			GL_FLOAT,	//type
			GL_FALSE,	//normalised?
			0,			//stride
			(void*)0	//array buffer offset
		);

		// 2nd attribute buffer : colors
		glEnableVertexAttribArray(1);
		glBindBuffer(GL_ARRAY_BUFFER, colorbuffer);
		glVertexAttribPointer(
			1,                                // attribute. No particular reason for 1, but must match the layout in the shader.
			3,                                // size
			GL_FLOAT,                         // type
			GL_FALSE,                         // normalized?
			0,                                // stride
			(void*)0                          // array buffer offset
		);
		
		//enable depth test 
		glEnable(GL_DEPTH_TEST);
		//accept frag if closer
		glDepthFunc(GL_LESS);

		//the triangle is then drawn
		glDrawArrays(GL_TRIANGLES, 0, 12*3);

		glDisableVertexAttribArray(0);

		glDisableVertexAttribArray(1);

		// Swap buffers
		glfwSwapBuffers(window);
		glfwPollEvents();

	} // Check if the ESC key was pressed or the window was closed
	while (glfwGetKey(window, GLFW_KEY_ESCAPE) != GLFW_PRESS &&
		glfwWindowShouldClose(window) == 0);

	//cleanup VBO and shader
	glDeleteBuffers(1, &vertexbuffer);
	glDeleteBuffers(1, &colorbuffer);

	
	glDeleteProgram(programID);
	glDeleteTextures(1, &Texture);
	glDeleteVertexArrays(1, &VertexArrayID);
	// Close OpenGL window and terminate GLFW
	glfwTerminate();


}


